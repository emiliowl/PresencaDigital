    public void carregaProfessores() {
        this.professores = professorDAO.verTodos();
        for (int i = 0; i < professores.size(); i++) {
            this.jComboBox1.addItem(professores.get(i));
        }
    }

    public void carregaMaterias(Professor professor) {
        this.materias = materiaDAO.buscaPorColuna("id_professor", "" + professor.getProfessorID());//gambiarra para passar parametro id_professor como String
        Materia[] listData = new Materia[materias.size()];
        for (int i = 0; i < materias.size(); i++) {
            listData[i] = materias.get(i);
        }
        this.jList1.setListData(listData);
    }

    public void carregaAlunos(Materia materia) {
        Aluno[] listData = new Aluno[materia.getAlunos().size()];
        for (int i = 0; i < materia.getAlunos().size(); i++) {
            listData[i] = materia.getAlunos().get(i);
        }
        this.jList2.setListData(listData);
    }
