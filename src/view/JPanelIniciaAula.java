    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //verifica se o professor esta com outra aula iniciada
        Aula aula = aulaDAO.verificarAulaIniciada(this.professor);
        if(aula == null) {
            //se o professor nao esta em outra aula, entao inicia nova aula
            aula = new Aula(0 , materia, new Date(), null, new Date());
            this.controladorJanelas.navegar(TelaEscolhida.AULA, aula);
        } else {
            //carrega a materia completamente
            aula.getMateria().setAlunos(this.materiaDAO.carregarAlunos(aula.getMateria()));
            //se o professor esta em outra aula, pergunta se ele deseja recupera-la
            int opcao = JOptionPane.showConfirmDialog(controladorJanelas.getUi(), "Professor ja possui aula iniciada: \nMateria: " +
                    aula.getMateria().getNome() + "\nData: " + aula.getData().toString() + "\nHora de inicio: " + aula.getHoraInicio() +
                    "\nDeseja recupera-la?");
            switch (opcao) {
                case 0:
                    this.controladorJanelas.navegar(TelaEscolhida.AULA, aula);
                case 1:
                case 2:
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed


